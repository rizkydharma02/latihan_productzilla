// Variable
let a = 10;
let b = 20;
let c = 21;
let d = 10;
let e = '10';

console.log('a = ', a, 'b = ', b, 'c = ', c, 'd = ', d, 'e = ', e);

console.log('a > b adalah ', a > b); //lebih dari
console.log('a < b adalah ', a < b); // kurang dari
console.log('a >= b adalah ', a >= b); // lebih dari sama dengan
console.log('a <= d adalah ', a <= d); // kurang dari sama dengan

console.log('a == e adalah ', a == e); // sama dengan (tidak memandang tioe data sama) (true)
console.log('a === e adalah ', a === e); // sama dengan (memandang tipe data yang sama) (false)

console.log('a != e adalah ', a != e); // tidak sama dengan (tidak memandang tipe data beda) (false)
console.log('a !== e adalah ', a !== e); // tidak sama dengan (memandang tipe data yang beda) (true)
