//Variable
let a = 1;

console.log('a = ', a);
console.log('a + 3 = ', a + 3); // Pertambahan
console.log('a - 10 = ', a - 10); // Pengurangan
console.log('a * 10 = ', a * 10); // Perkalian
console.log('a ** 10 = ', a ** 10); // Perpangkatan
console.log('a / 10 = ', a / 10); // Pembagian
console.log('a % 1 = ', a % 1); // Sisa Bagi

//Campuran Operator Aritmatika
console.log('a / 10 X 2 / 3 + 5 = ', ((a / 10) * 2) / 3 + 5);

//Penambahan Isi Variable String
const namaDepan = 'Rizky';
const namaBelakang = 'Dharma';
console.log(namaDepan + namaBelakang); //Penambahan Value String atau Concat String
console.log(namaDepan - namaBelakang); // Nan Value
console.log(namaDepan * namaBelakang); // Nan Value
